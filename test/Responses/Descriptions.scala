<TH ALIGN="center"  class="h" >Subject Description</TH>
<TR  class="r" >
<TD ALIGN="" DP="" ROWSPAN=""  COLSPAN=""  class="d" >
This subject examines a broad range of visualisation techniques used in industry to assist researchers in analysis and interpretation of data. It introduces general techniques for the display of univariate, multivariate and vector data in one, two and higher independent dimensions. The underlying geometric computational techniques are presented as well as their application in specific fields. Topics include such areas as splines, contours, Voronoi diagrams, height fields, vector fields, atomic modelling and 3D scalar fields.; Research papers provide source material for the majority of this subject.<br> <br>
